"""
src/ui/font.py  ·  CAMADA VIEW (utilitário)
============================================
Renderização de texto bitmap usando GL_POINTS.

Fonte
-----
Bitmap 5×7 pixels por caractere.  Cada entrada do dicionário FONT_5x7
é uma lista de 7 inteiros (linhas de cima para baixo).  Cada inteiro
representa uma linha de 5 pixels com o bit 4 (0b10000) sendo o pixel
mais à esquerda.

Renderização
------------
draw_text(text, x, y, color, scale) percorre cada caractere, cada
linha e cada bit, emitindo glVertex2f para pixels ON.  Com scale=2
cada "pixel fonte" vira um bloco 2×2 na tela, produzindo texto
legível de ~10×14 pixels por caractere.

Nenhuma biblioteca de fonte externa é usada — apenas OpenGL puro.
"""

