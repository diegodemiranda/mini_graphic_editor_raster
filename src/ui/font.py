# Largura de um caractere incluindo espaçamento: (5 bits + 1 gap) × scale
CHAR_WIDTH = 6  # em "font-pixels" (antes da escala)
CHAR_HEIGHT = 7  # em "font-pixels"


def draw_text(
    text: str, x: int, y: int, color: tuple = (1.0, 1.0, 1.0), scale: int = 2
) -> None:
    """
    Renderiza `text` como bitmap 5×7 escalado por `scale`.

    Parâmetros
    ----------
    text  : string ASCII (convertida para maiúsculas internamente)
    x, y  : posição superior-esquerda em coordenadas de janela
    color : tupla RGB normalizada (0.0–1.0)
    scale : cada "pixel fonte" ocupa scale×scale pixels na tela

    Usa GL_POINTS — nenhum texto de biblioteca envolvido.
    """
    glColor3f(*color)
    glBegin(GL_POINTS)

    cx = x
    for ch in text.upper():
        rows = FONT_5x7.get(ch)
        if rows is None:  # caractere desconhecido → espaço
            cx += CHAR_WIDTH * scale
            continue
        for row_idx, row_bits in enumerate(rows):
            for bit in range(5):
                if row_bits & (0x10 >> bit):  # bit 4 = pixel esquerdo
                    px = cx + bit * scale
                    py = y + row_idx * scale
                    # Bloco scale×scale pixels
                    for dy in range(scale):
                        for dx in range(scale):
                            glVertex2f(px + dx, py + dy)
        cx += CHAR_WIDTH * scale

    glEnd()


def text_width(text: str, scale: int = 2) -> int:
    """Retorna a largura em pixels de `text` com a escala dada."""
    return len(text) * CHAR_WIDTH * scale


def text_height(scale: int = 2) -> int:
    """Altura em pixels da fonte com a escala dada."""
    return CHAR_HEIGHT * scale
